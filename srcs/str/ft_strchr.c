char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	if (s[i] == '\0' && c == 0)
		return ((char *)&s[i]);
	while (s[i] && s[i] != (char)c)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	if (s[i] == (char)c)
		return ((char *)&s[i]);
	return (0);
}
